
import React, { useState, useMemo, useEffect } from 'react';
import { Quote, QuoteItem, Product, CarPart, ToastMessage, WorkOrder, User } from '../types';
import { useSupabase } from '../contexts/SupabaseContext';


interface QuotesProps {
  addToast: (message: string, type: ToastMessage['type']) => void;
  showConfirmation: (message: string, onConfirm: () => void) => void;
  addActivityLog: (action: string) => void;
  currentUser: User;
}

export const Quotes: React.FC<QuotesProps> = ({ addToast, showConfirmation, addActivityLog, currentUser }) => {
  const supabase = useSupabase();
  const [quotes, setQuotes] = useState<Quote[]>([]);
  const [products, setProducts] = useState<Product[]>([]);
  const [carParts, setCarParts] = useState<CarPart[]>([]);
  const [workOrders, setWorkOrders] = useState<WorkOrder[]>([]);

  useEffect(() => {
    const fetchData = async () => {
        const [q, p, cp, wo] = await Promise.all([
            supabase.from('quotes').select('*'),
            supabase.from('products').select('*'),
            supabase.from('car_parts').select('*'),
            supabase.from('work_orders').select('*'),
        ]);
        if(q.data) setQuotes(q.data as unknown as Quote[]);
        if(p.data) setProducts(p.data);
        if(cp.data) setCarParts(cp.data);
        if(wo.data) setWorkOrders(wo.data as unknown as WorkOrder[]);
    };
    fetchData();

    const channel = supabase.channel('quotes-db-changes');
    channel.on('postgres_changes', { event: '*', schema: 'public', table: 'quotes' }, payload => {
        if(payload.eventType === 'INSERT') setQuotes(c => [...c, payload.new as Quote]);
        if(payload.eventType === 'UPDATE') setQuotes(c => c.map(i => i.id === payload.new.id ? payload.new as Quote : i));
        if(payload.eventType === 'DELETE') setQuotes(c => c.filter(i => i.id !== (payload.old as Quote).id));
    }).on('postgres_changes', { event: '*', schema: 'public', table: 'work_orders' }, payload => {
        if(payload.eventType === 'INSERT') setWorkOrders(c => [...c, payload.new as WorkOrder]);
        if(payload.eventType === 'UPDATE') setWorkOrders(c => c.map(i => i.id === payload.new.id ? payload.new as WorkOrder : i));
        if(payload.eventType === 'DELETE') setWorkOrders(c => c.filter(i => i.id !== (payload.old as WorkOrder).id));
    }).subscribe();

    return () => {
        supabase.removeChannel(channel);
    }
  }, [supabase]);

  const [view, setView] = useState<'list' | 'edit'>('list');
  const [currentQuote, setCurrentQuote] = useState<Quote | null>(null);
  
  const [newItemName, setNewItemName] = useState('');
  const [newItemQty, setNewItemQty] = useState<number | string>(1);
  // Fix: Correctly type the search results to be an intersection so `type` is always available
  const [searchResults, setSearchResults] = useState<((Product | CarPart) & { type: 'product' | 'carPart' })[]>([]);

  useEffect(() => {
    if (newItemName.trim() === '') {
      setSearchResults([]);
      return;
    }

    const combinedSearches = [
      ...products
        .filter(p => p.name.toLowerCase().includes(newItemName.toLowerCase()))
        .map(p => ({ ...p, type: 'product' as const })),
      ...carParts
        .filter(p => p.name.toLowerCase().includes(newItemName.toLowerCase()))
        .map(p => ({ ...p, type: 'carPart' as const })),
    ];
    setSearchResults(combinedSearches.slice(0, 10)); // Limit results
  }, [newItemName, products, carParts]);

  const handleNewQuote = () => {
    setCurrentQuote({
      id: '', // Will be generated by database
      title: 'Novo Orçamento',
      created_at: new Date().toISOString(),
      items: [],
    });
    setView('edit');
  };

  const handleEditQuote = (quote: Quote) => {
    setCurrentQuote({ ...quote });
    setView('edit');
  };

  const handleSaveQuote = async () => {
    if (!currentQuote || !currentQuote.title.trim()) {
      addToast('O título do orçamento não pode estar vazio.', 'error');
      return;
    }

    const quoteData = {
        title: currentQuote.title,
        items: currentQuote.items as any,
    };

    if (currentQuote.id) { // Update
        const { error } = await supabase.from('quotes').update(quoteData).eq('id', currentQuote.id);
        if (error) {
            addToast(`Erro ao salvar: ${error.message}`, 'error');
        } else {
            addToast('Orçamento salvo com sucesso!', 'success');
            addActivityLog(`atualizou o orçamento: ${quoteData.title}`);
        }
    } else { // Insert
        const { error } = await supabase.from('quotes').insert(quoteData);
        if (error) {
            addToast(`Erro ao criar: ${error.message}`, 'error');
        } else {
            addToast('Orçamento criado com sucesso!', 'success');
            addActivityLog(`criou o orçamento: ${quoteData.title}`);
        }
    }
    
    setView('list');
    setCurrentQuote(null);
  };
  
  const handleDeleteQuote = (id: string, title: string) => {
    showConfirmation('Tem certeza que deseja excluir este orçamento?', async () => {
      const { error } = await supabase.from('quotes').delete().eq('id', id);
      if (error) {
        addToast(`Erro ao excluir: ${error.message}`, 'error');
      } else {
        addToast('Orçamento excluído com sucesso!', 'success');
        addActivityLog(`excluiu o orçamento: ${title}`);
      }
    });
  };

  const handleAddItem = (item: (Product | CarPart) & { type: 'product' | 'carPart' } | null) => {
    if ((!item && !newItemName.trim()) || Number(newItemQty) <= 0) {
      addToast('Nome do item e quantidade são obrigatórios.', 'error');
      return;
    }
    
    let newItem: QuoteItem;
    if (item) { // Added from search
      newItem = {
        id: `item-${Date.now()}`,
        sourceId: item.id,
        name: item.name,
        quantity: Number(newItemQty),
        type: item.type
      };
    } else { // Manual item
      newItem = {
        id: `item-${Date.now()}`,
        name: newItemName,
        quantity: Number(newItemQty),
        type: 'manual'
      };
    }

    setCurrentQuote(prev => prev ? ({ ...prev, items: [...prev.items, newItem] }) : null);
    setNewItemName('');
    setNewItemQty(1);
    setSearchResults([]);
  };

  const handleRemoveItem = (itemId: string) => {
    setCurrentQuote(prev => prev ? ({ ...prev, items: prev.items.filter(i => i.id !== itemId) }) : null);
  };
  
  const handleCreateWorkOrder = (quote: Quote) => {
    showConfirmation(`Deseja criar uma Ordem de Serviço a partir do orçamento "${quote.title}"?`, async () => {
        const newWorkOrder: Omit<WorkOrder, 'id' | 'user_id'> & { items: any } = {
            title: quote.title,
            status: 'open',
            created_at: new Date().toISOString(),
            items: quote.items as any,
        };
        const { error } = await supabase.from('work_orders').insert(newWorkOrder);
        if (error) {
            addToast(`Erro ao criar OS: ${error.message}`, 'error');
        } else {
            addToast(`Ordem de Serviço "${quote.title}" criada com sucesso!`, 'success');
            addActivityLog(`criou OS a partir do orçamento "${quote.title}"`);
        }
    });
  }
  
  if (view === 'edit' && currentQuote) {
    return (
      <div className="space-y-6">
        <button onClick={() => setView('list')} className="flex items-center text-sm text-blue-600 dark:text-blue-400 hover:underline">
          <svg className="w-4 h-4 mr-1" fill="none" stroke="currentColor" viewBox="0 0 24 24" xmlns="http://www.w3.org/2000/svg"><path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M15 19l-7-7 7-7"></path></svg>
          Voltar para a lista
        </button>

        <div className="bg-white dark:bg-gray-800 p-6 rounded-lg shadow-md">
          <input
            type="text"
            value={currentQuote.title}
            onChange={e => setCurrentQuote({ ...currentQuote, title: e.target.value })}
            placeholder="Título do Orçamento"
            className="text-2xl font-bold w-full bg-transparent border-b-2 border-gray-200 dark:border-gray-700 focus:border-primary dark:focus:border-accent focus:outline-none pb-2 mb-6 text-gray-800 dark:text-gray-200"
          />

          {/* Add item form */}
          <div className="bg-gray-50 dark:bg-gray-700/50 p-4 rounded-lg">
            <h3 className="font-semibold mb-2 text-gray-700 dark:text-gray-300">Adicionar Item</h3>
            <div className="flex flex-col sm:flex-row gap-2 relative">
                <div className="flex-grow">
                    <input 
                        type="text" 
                        placeholder="Buscar produto/peça ou digitar nome manual"
                        value={newItemName}
                        onChange={e => setNewItemName(e.target.value)}
                        className="w-full px-3 py-2 border rounded-md dark:bg-gray-800 dark:border-gray-600"
                    />
                    {searchResults.length > 0 && (
                        <ul className="absolute z-10 w-full sm:w-1/2 bg-white dark:bg-gray-800 border dark:border-gray-600 rounded-md mt-1 max-h-60 overflow-y-auto shadow-lg">
                            {searchResults.map(item => (
                                <li key={`${item.type}-${item.id}`} 
                                    onClick={() => { handleAddItem(item); }}
                                    className="px-4 py-2 cursor-pointer hover:bg-gray-100 dark:hover:bg-gray-700 flex justify-between">
                                    <span>{item.name}</span>
                                    <span className={`text-xs px-2 py-1 rounded-full ${item.type === 'product' ? 'bg-blue-100 text-blue-800 dark:bg-blue-900 dark:text-blue-300' : 'bg-green-100 text-green-800 dark:bg-green-900 dark:text-green-300'}`}>
                                        {item.type === 'product' ? 'Estoque' : 'Peças'}
                                    </span>
                                </li>
                            ))}
                        </ul>
                    )}
                </div>
                <input 
                    type="number" 
                    placeholder="Qtd" 
                    value={newItemQty}
                    onChange={e => setNewItemQty(e.target.value)}
                    min="0.01" step="0.01"
                    className="w-full sm:w-24 px-3 py-2 border rounded-md dark:bg-gray-800 dark:border-gray-600"
                />
                <button onClick={() => handleAddItem(null)} className="bg-blue-600 text-white px-4 py-2 rounded-md hover:bg-blue-700 transition">Adicionar Manual</button>
            </div>
          </div>
          
          {/* Items list */}
          <div className="mt-6">
            <h3 className="font-semibold text-gray-800 dark:text-gray-200 mb-2">Itens no Orçamento</h3>
            <ul className="space-y-2">
              {currentQuote.items.length > 0 ? currentQuote.items.map(item => (
                <li key={item.id} className="flex items-center justify-between p-3 bg-gray-50 dark:bg-gray-700/50 rounded-md">
                    <div>
                        <span className="font-medium text-gray-800 dark:text-gray-200">{item.name}</span>
                        {item.type !== 'manual' && <span className={`ml-2 text-xs px-2 py-0.5 rounded-full ${item.type === 'product' ? 'bg-blue-100 text-blue-800 dark:bg-blue-900 dark:text-blue-300' : 'bg-green-100 text-green-800 dark:bg-green-900 dark:text-green-300'}`}>{item.type === 'product' ? 'Estoque' : 'Peça'}</span>}
                    </div>
                    <div className="flex items-center space-x-4">
                        <span className="text-gray-600 dark:text-gray-400">Qtd: {item.quantity}</span>
                        <button onClick={() => handleRemoveItem(item.id)} className="text-red-500 hover:text-red-700">
                            <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path strokeLinecap="round" strokeLinejoin="round" strokeWidth="2" d="M6 18L18 6M6 6l12 12"></path></svg>
                        </button>
                    </div>
                </li>
              )) : <p className="text-center text-gray-500 dark:text-gray-400 py-4">Nenhum item adicionado.</p>}
            </ul>
          </div>
          <div className="flex justify-end mt-8">
            <button onClick={handleSaveQuote} className="bg-primary text-white px-6 py-2 rounded-lg shadow hover:bg-secondary transition">Salvar Orçamento</button>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className="space-y-6">
      <div className="flex justify-between items-center">
        <div>
          <h2 className="text-2xl font-bold text-gray-800 dark:text-gray-200">Orçamentos / Anotações</h2>
          <p className="text-gray-500 dark:text-gray-400">Crie e gerencie suas listas de materiais para orçamentos.</p>
        </div>
        <button onClick={handleNewQuote} className="bg-primary text-white px-4 py-2 rounded-lg shadow hover:bg-secondary transition">
          Novo Orçamento
        </button>
      </div>

      <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
        {quotes.sort((a,b) => new Date(b.created_at).getTime() - new Date(a.created_at).getTime()).map(quote => (
            <div key={quote.id} className="bg-white dark:bg-gray-800 p-5 rounded-lg shadow-md flex flex-col justify-between">
                <div>
                    <h3 className="font-bold text-lg text-gray-800 dark:text-gray-200">{quote.title}</h3>
                    <p className="text-sm text-gray-500 dark:text-gray-400">Criado em: {new Date(quote.created_at).toLocaleDateString('pt-BR')}</p>
                    <p className="text-sm text-gray-600 dark:text-gray-300 mt-2">{quote.items.length} item(s)</p>
                </div>
                <div className="flex justify-end items-center space-x-2 mt-4 border-t dark:border-gray-700 pt-4">
                    <button onClick={() => handleCreateWorkOrder(quote)} title="Criar Ordem de Serviço" className="text-green-600 hover:text-green-900 dark:text-green-400 dark:hover:text-green-300 p-1 rounded-full hover:bg-gray-200 dark:hover:bg-gray-700 transition">
                        <svg xmlns="http://www.w3.org/2000/svg" className="h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                          <path strokeLinecap="round" strokeLinejoin="round" d="M9 5H7a2 2 0 00-2 2v12a2 2 0 002 2h10a2 2 0 002-2V7a2 2 0 00-2-2h-2M9 5a2 2 0 002 2h2a2 2 0 002-2M9 5a2 2 0 012-2h2a2 2 0 012 2m-6 9l2 2 4-4" />
                        </svg>
                    </button>
                    <button onClick={() => handleEditQuote(quote)} title="Editar" className="text-indigo-600 hover:text-indigo-900 dark:text-indigo-400 dark:hover:text-indigo-300 p-1 rounded-full hover:bg-gray-200 dark:hover:bg-gray-700 transition">
                       <svg xmlns="http://www.w3.org/2000/svg" className="h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                            <path strokeLinecap="round" strokeLinejoin="round" d="M11 5H6a2 2 0 00-2 2v11a2 2 0 002 2h11a2 2 0 002-2v-5m-1.414-9.414a2 2 0 112.828 2.828L11.828 15H9v-2.828l8.586-8.586z" />
                        </svg>
                    </button>
                    <button onClick={() => handleDeleteQuote(quote.id, quote.title)} title="Excluir" className="text-red-600 hover:text-red-900 dark:text-red-400 dark:hover:text-red-300 p-1 rounded-full hover:bg-gray-200 dark:hover:bg-gray-700 transition">
                        <svg xmlns="http://www.w3.org/2000/svg" className="h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                            <path strokeLinecap="round" strokeLinejoin="round" d="M19 7l-.867 12.142A2 2 0 0116.138 21H7.862a2 2 0 01-1.995-1.858L5 7m5 4v6m4-6v6m1-10V4a1 1 0 00-1-1h-4a1 1 0 00-1 1v3M4 7h16" />
                        </svg>
                    </button>
                </div>
            </div>
        ))}
      </div>
    </div>
  );
};
